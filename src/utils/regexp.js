module.exports = {
  // 只包含字母或数字
  letterOrNum: /^[A-Za-z0-9]+$/,
  // 邮箱
  email: /^(([^<>()[\]\\.,;:\s@"]+(\.[^<>()[\]\\.,;:\s@"]+)*)|(".+"))@((\[[0-9]{1,3}\.[0-9]{1,3}\.[0-9]{1,3}\.[0-9]{1,3}\])|(([a-zA-Z\-0-9]+\.)+[a-zA-Z]{2,}))$/,
  // 必须包含字母数字和下划线
  letterNumULine: /(?=.*[A-Za-z])(?=.*[\d])(?=.*_)/,
}